#include "3-calc.h"


/**
*op_add - sum of two numbers
*@a: the 1st num
*@b: the 2nd num
*Return: the sum of two numbers
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - substraction of two numbers
*@a: the 1st num
*@b: the 2nd num
*Return: the substraction of two numbers
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - multiply of two numbers
*@a: the 1st num
*@b: the 2nd num
*Return: multiply of two numbers
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
*op_div - division of two numbers
*@a: the 1st num
*@b: the 2nd num
*Return: division of two numbers
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - modulus two numbers
*@a: the 1st num
*@b: the 2nd num
*Return: modulus two numbers
*/
int op_mod(int a, int b)
{
	return (a % b);
}
